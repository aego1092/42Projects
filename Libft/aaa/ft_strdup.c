#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*str;
	size_t	n;
	size_t	i;

	i = 0;
	n = ft_strlen(s);
	str = (char *) malloc((n + 1) * sizeof(char));
	if (!str)
		return (NULL);
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
